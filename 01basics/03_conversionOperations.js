let score = 12

// Convert score to string
let scoreAsString = String(score)
console.log(scoreAsString);
console.log(typeof scoreAsString);

// Convert score to boolean
let scoreAsBoolean = Boolean(score)
console.log(scoreAsBoolean);
console.log(typeof scoreAsBoolean);

// Convert score to number
let scoreAsNumber = Number(score)
console.log(scoreAsNumber);
console.log(typeof scoreAsNumber);





